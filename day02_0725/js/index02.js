var result = 0;
let num1 = 5;

// = 대입연산자
result = num1;
console.log(`[ = 대입연산자] ${result}`);

// += 누적 대입연산자
result += num1; // result = result + num1
console.log(`[ 누적 += 대입연산자] ${result}`);

// -= 대입연산자
result -= num1; // result = result - num1
console.log(`[-= 대입연산자] ${result}`);

// *= 대입연산자
result *= num1; // result = result * num1
console.log(`[*= 대입연산자] ${result}`);

// /= 대입연산자
result /= num1; // result = result / num1
console.log(`[/= 대입연산자] ${result}`);

// %= 대입연산자
result %= num1; // result = result % num1
console.log(`[%= 대입연산자] ${result}`);